package com.tripinseoul.VO;

public class Cat2VO {
	
	private String c2_id;
	private String c2_img;
	
	public String getC2_id() {
		return c2_id;
	}
	public void setC2_id(String c2_id) {
		this.c2_id = c2_id;
	}
	public String getC2_img() {
		return c2_img;
	}
	public void setC2_img(String c2_img) {
		this.c2_img = c2_img;
	}
	@Override
	public String toString() {
		return "Cat2VO [c2_id=" + c2_id + ", c2_img=" + c2_img + "]";
	}
	
	
	
	
	
}

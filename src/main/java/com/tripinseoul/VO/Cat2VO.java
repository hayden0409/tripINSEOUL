package com.tripinseoul.VO;

public class Cat2VO {
	private String id;
	private String img;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	@Override
	public String toString() {
		return "Cat2VO [id=" + id + ", img=" + img + "]";
	}
	
	
	
}

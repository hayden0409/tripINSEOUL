package com.tripinseoul.VO;

public class Cat2VO {
	
	private String c2_id;
	private String c2_name;
	public String getC2_id() {
		return c2_id;
	}
	public void setC2_id(String c2_id) {
		this.c2_id = c2_id;
	}
	public String getC2_name() {
		return c2_name;
	}
	public void setC2_name(String c2_name) {
		this.c2_name = c2_name;
	}
	@Override
	public String toString() {
		return "Cat2VO [c2_id=" + c2_id + ", c2_name=" + c2_name + "]";
	}
	
	
	
	
	
	
}
